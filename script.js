function buildTypesGrid() {
  const grid = document.getElementById('types-grid');
  if (!grid) return;
  grid.innerHTML = '';

  Object.entries(types).forEach(([code, type]) => {
    // Card wrapper
    const wrapper = document.createElement('div');
    wrapper.className = 'type-card-wrapper';

    // Card (clickable header)
    const card = document.createElement('div');
    card.className = 'type-card';
    card.setAttribute('data-code', code);
    card.innerHTML = `
      <div class="type-card-animal">${type.animal}</div>
      <div class="type-card-code">${code}</div>
      <div class="type-card-name">${type.name}</div>
      <div class="type-card-catch">${type.catchcopy}</div>
      <div class="type-card-arrow">▼</div>
    `;

    // Detail panel (accordion)
    const detail = document.createElement('div');
    detail.className = 'type-detail';
    detail.setAttribute('data-code', code);

    // Build detail content
    const paragraphs = type.fullDescription.split('\n\n').map(p => p.trim()).filter(Boolean);
    const paraHTML = paragraphs.map(p => `<p class="type-detail-para">${p}</p>`).join('');

    const strengthsHTML = type.strengths.map(s => `<li>${s}</li>`).join('');
    const fitHTML = type.fit.map(f => `<li>${f}</li>`).join('');
    const celebHTML = type.celebrities.map(c =>
      `<li class="celeb-item"><span class="celeb-name">${c.name}</span><span class="celeb-comment">${c.comment}</span></li>`
    ).join('');

    detail.innerHTML = `
      <div class="type-detail-inner">
        <div class="type-detail-desc">${paraHTML}</div>
        <div class="type-detail-cols">
          <div class="type-detail-col">
            <h4 class="type-detail-subtitle">強み</h4>
            <ul class="type-detail-list">${strengthsHTML}</ul>
          </div>
          <div class="type-detail-col">
            <h4 class="type-detail-subtitle">向いているフィールド</h4>
            <ul class="type-detail-list">${fitHTML}</ul>
          </div>
        </div>
        <div class="type-detail-celeb">
          <h4 class="type-detail-subtitle">このタイプの有名人</h4>
          <ul class="celeb-list">${celebHTML}</ul>
        </div>
      </div>
    `;

    // Toggle accordion
    card.addEventListener('click', () => {
      const isOpen = detail.classList.contains('open');
      // Close all others
      document.querySelectorAll('.type-detail.open').forEach(d => {
        d.classList.remove('open');
        const sibCard = d.previousElementSibling;
        if (sibCard) sibCard.classList.remove('active');
      });
      if (!isOpen) {
        detail.classList.add('open');
        card.classList.add('active');
        // scroll into view after transition
        setTimeout(() => {
          detail.scrollIntoView({ behavior: 'smooth', block: 'nearest' });
        }, 50);
      }
    });

    wrapper.appendChild(card);
    wrapper.appendChild(detail);
    grid.appendChild(wrapper);
  });
}

