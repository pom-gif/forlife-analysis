function showResult() {
  const { code, scores, riasecScores, riasecTop2 } = calcTypeCode();
  const type = types[code] || types['ELPC']; // fallback (shouldn't happen)

  // Header
  document.getElementById('result-animal').textContent = type.animal;
  document.getElementById('result-type-code').textContent = code;
  document.getElementById('result-name').textContent = type.name;
  document.getElementById('result-title').textContent = type.title;

  // Use fullDescription with paragraph breaks
  const descEl = document.getElementById('result-description');
  descEl.innerHTML = '';
  const paragraphs = type.fullDescription.split('\n\n');
  paragraphs.forEach(para => {
    if (para.trim()) {
      const p = document.createElement('p');
      p.textContent = para.trim();
      descEl.appendChild(p);
    }
  });

  // Strengths
  const strengthList = document.getElementById('strength-list');
  strengthList.innerHTML = '';
  type.strengths.forEach(s => {
    const li = document.createElement('li');
    li.textContent = s;
    strengthList.appendChild(li);
  });

  // Fit fields
  const fitList = document.getElementById('fit-list');
  fitList.innerHTML = '';
  type.fit.forEach(f => {
    const li = document.createElement('li');
    li.textContent = f;
    fitList.appendChild(li);
  });

  // Celebrities
  const celebList = document.getElementById('celeb-list');
  celebList.innerHTML = '';
  if (type.celebrities && type.celebrities.length > 0) {
    type.celebrities.forEach(celeb => {
      const li = document.createElement('li');
      li.className = 'celeb-item';
      li.innerHTML = `<span class="celeb-name">${celeb.name}</span><span class="celeb-comment">${celeb.comment}</span>`;
      celebList.appendChild(li);
    });
  }

  // Axes chart
  renderAxesChart(scores);

  // RIASEC chart
  renderRiasecChart(riasecScores, riasecTop2);

  // Share button
  btnShare.onclick = () => {
    const text = encodeURIComponent(`私は${type.name}（${type.title}）でした！ #forlife診断`);
    const url = encodeURIComponent(window.location.href);
    window.open(`https://twitter.com/intent/tweet?text=${text}&url=${url}`, '_blank');
  };

  showScreen(screenResult);
}

function renderAxesChart(scores) {
  const container = document.getElementById('axes-chart');
  container.innerHTML = '';

  axisInfo.forEach((info, i) => {
    const score = scores[i]; // 0–10, A side
    const pct = (score / 10) * 100;

    const row = document.createElement('div');
    row.className = 'axis-row';

    // Labels row
    const labelsDiv = document.createElement('div');
    labelsDiv.className = 'axis-row-labels';

    const leftLabel = document.createElement('span');
    leftLabel.className = 'axis-label-left';
    leftLabel.textContent = info.left;

    const rightLabel = document.createElement('span');
    rightLabel.className = 'axis-label-right';
    rightLabel.textContent = info.right;

    labelsDiv.appendChild(leftLabel);
    labelsDiv.appendChild(rightLabel);

    // Bar
    const track = document.createElement('div');
    track.className = 'axis-bar-track';

    const fill = document.createElement('div');
    fill.className = 'axis-bar-fill';
    fill.style.width = '0%'; // animate from 0
    track.appendChild(fill);

    // Score label
    const scoreLabel = document.createElement('div');
    scoreLabel.className = 'axis-score-label';
    scoreLabel.textContent = `${score} / 10`;

    row.appendChild(labelsDiv);
    row.appendChild(track);
    row.appendChild(scoreLabel);
    container.appendChild(row);

    // Animate after paint
    requestAnimationFrame(() => {
      requestAnimationFrame(() => {
        fill.style.width = pct + '%';
      });
    });
  });
}

/* ============================================================
   RIASEC Chart Rendering
============================================================ */
function renderRiasecChart(riasecScores, riasecTop2) {
  const riasecLabels = {
    R: { label: 'R：現実型', desc: '実体のある成果物・技術・現場を重視' },
    I: { label: 'I：研究型', desc: '分析・知識・深掘りを重視' },
    A: { label: 'A：芸術型', desc: '表現・創造・独自スタイルを重視' },
    S: { label: 'S：社会型', desc: '人間関係・支援・教育を重視' },
    E: { label: 'E：企業家型', desc: 'リーダーシップ・交渉・経営を重視' },
    C: { label: 'C：管理型', desc: '秩序・正確さ・計画を重視' }
  };

  const chartContainer = document.getElementById('riasec-chart');
  const topContainer = document.getElementById('riasec-top');
  if (!chartContainer || !topContainer) return;

  chartContainer.innerHTML = '';
  topContainer.innerHTML = '';

  Object.entries(riasecScores).forEach(([key, score]) => {
    const isTop = riasecTop2.includes(key);
    const info = riasecLabels[key];
    const pct = (score / 3) * 100;

    const row = document.createElement('div');
    row.className = 'riasec-row' + (isTop ? ' riasec-row-top' : '');

    row.innerHTML = `
      <div class="riasec-row-header">
        <span class="riasec-label${isTop ? ' riasec-label-top' : ''}">${info.label}</span>
        <span class="riasec-desc">${info.desc}</span>
        <span class="riasec-score">${score} / 3</span>
      </div>
      <div class="riasec-bar-track">
        <div class="riasec-bar-fill${isTop ? ' riasec-bar-fill-top' : ''}" style="width: 0%" data-pct="${pct}"></div>
      </div>
    `;

    chartContainer.appendChild(row);
  });

  // Animate bars
  requestAnimationFrame(() => {
    requestAnimationFrame(() => {
      chartContainer.querySelectorAll('.riasec-bar-fill').forEach(fill => {
        fill.style.width = fill.dataset.pct + '%';
      });
    });
  });

  // Top 2 summary
  const topHTML = riasecTop2.map(key => {
    const info = riasecLabels[key];
    return `<span class="riasec-top-badge">${info.label}</span>`;
  }).join('');
  topContainer.innerHTML = `<p class="riasec-top-text">あなたの上位志向：${topHTML}</p>`;
}

