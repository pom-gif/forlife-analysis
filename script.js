function renderQuestion(index) {
  const q = questions[index];

  // Progress
  progressLabel.textContent = `Q${index + 1} / ${questions.length}`;
  if (typeof q.axis === 'number') {
    progressAxisLabel.textContent = axisInfo[q.axis].label;
  } else if (q.axis.startsWith('riasec_')) {
    progressAxisLabel.textContent = 'ビジネス志向';
  } else {
    progressAxisLabel.textContent = '価値観';
  }
  const pct = ((index) / questions.length) * 100;
  progressFill.style.width = pct + '%';

  // Question content with re-trigger animation
  questionWrap.style.animation = 'none';
  questionWrap.offsetHeight; // reflow
  questionWrap.style.animation = '';

  qNumber.textContent = `Q${index + 1}`;
  qText.textContent = q.text;
  choiceAText.textContent = q.a;
  choiceBText.textContent = q.b;

  // Back button visibility
  if (index === 0) {
    btnBack.classList.add('hidden');
  } else {
    btnBack.classList.remove('hidden');
  }
}

/* ============================================================
   Answer Handler
============================================================ */
function handleAnswer(choice) {
  answers[currentIndex] = choice;

  if (currentIndex < questions.length - 1) {
    currentIndex++;
    renderQuestion(currentIndex);
  } else {
    // All answered — show result
    showResult();
  }
}

/* ============================================================
   Scoring & Type Calculation
============================================================ */
function calcTypeCode() {
  const axisScores = [0, 0, 0, 0];
  const riasecScores = { R: 0, I: 0, A: 0, S: 0, E: 0, C: 0 };

  questions.forEach((q, i) => {
    const ans = answers[i];
    if (ans === 'a') {
      if (typeof q.axis === 'number') {
        axisScores[q.axis]++;
      } else if (q.axis.startsWith('riasec_')) {
        const key = q.axis.split('_')[1];
        riasecScores[key]++;
      }
    }
  });

  const chars = [
    axisScores[0] >= 5 ? 'E' : 'I',  // Axis1: E≥5 else I (10問)
    axisScores[1] >= 5 ? 'L' : 'S',  // Axis2: L≥5 else S (10問)
    axisScores[2] >= 5 ? 'P' : 'A',  // Axis3: P≥5 else A (10問)
    axisScores[3] >= 5 ? 'F' : 'C'   // Axis4: F≥5 else C (10問)
  ];

  // RIASEC上位2カテゴリを取得
  const riasecTop2 = Object.entries(riasecScores)
    .sort((a, b) => b[1] - a[1])
    .slice(0, 2)
    .map(([key]) => key);

  return { code: chars.join(''), scores: axisScores, riasecScores, riasecTop2 };
}

